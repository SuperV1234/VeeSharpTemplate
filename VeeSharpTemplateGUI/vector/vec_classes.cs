 	public class Vector2_int
 	{
 		public Vector2_int(int mX, int mY)
 		{
 			X = mX;
 			Y = mY;
 		}
 		public int X { get; set; }
 		public int Y { get; set; }
	 	public int ComponentSum()
	  	{
	 		return X + Y;
		}
	}
